    <h2>Frequently Asked Questions on Sexual Health</h2>
   <p> Source: http://www.saathii.org/sites/default/files/kp-hiv-orntn-handout-eng.pdf 
    </p>
    
   <p> What is sexual health? 
    </p>
     
    <p>Good health is considered to be a state of “complete physical, mental and social well being” and not merely the absence of diseases or infirmity. The principles of sexual health are the same: 
        </p>
   <p>“Physical well being” means good health and hygiene for your genitals and related systems. “Mental well being” means feeling okay about your sexual desires and needs and not guilty or depressed about them. “Social well being” means not facing social discrimination because of your sexual desires and needs, or because of any problem that may result from unsafe sex. 
        A combination of all these factors makes for a (sexually) healthy YOU!  
        </p>
    <p>b) What are HIV and AIDS? Are they the same? 
        </p>
     
   <p> HIV stands for “human immunodeficiency virus” and AIDS stands for “acquired immune deficiency syndrome”. In simple terms: HIV is a “virus” that makes the human body’s immune system weak or deficient, while AIDS is an “outcome of HIV infection”. However, being infected with HIV is not the same as having AIDS. </p>
     
    <p>AIDS is actually a late stage of HIV infection and comes about only when HIV has made your body’s immunity (defence system) too weak to fight off other infections. It may take several years for AIDS to develop. The healthier your body and the better you take care of it, the longer it will take for AIDS to develop. 
        </p>

